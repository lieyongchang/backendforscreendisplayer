package com.techstudio.model.dto;

public class LayoutDetailsObjDto {

	// variables
	public int layoutID;
	public String title;
	public String backgroundUrl;
	public String backgroundGradient;
	public String backgroundGradientDirection;
	public String cardGradient;
	public String cardGradientDirection;
	
	// getters & setters
	public int getLayoutID() {
		return layoutID;
	}
	public void setLayoutID(int layoutID) {
		this.layoutID = layoutID;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBackgroundUrl() {
		return backgroundUrl;
	}
	public void setBackgroundUrl(String backgroundUrl) {
		this.backgroundUrl = backgroundUrl;
	}
	public String getBackgroundGradient() {
		return backgroundGradient;
	}
	public void setBackgroundGradient(String backgroundGradient) {
		this.backgroundGradient = backgroundGradient;
	}
	public String getBackgroundGradientDirection() {
		return backgroundGradientDirection;
	}
	public void setBackgroundGradientDirection(String backgroundGradientDirection) {
		this.backgroundGradientDirection = backgroundGradientDirection;
	}
	public String getCardGradient() {
		return cardGradient;
	}
	public void setCardGradient(String cardGradient) {
		this.cardGradient = cardGradient;
	}
	public String getCardGradientDirection() {
		return cardGradientDirection;
	}
	public void setCardGradientDirection(String cardGradientDirection) {
		this.cardGradientDirection = cardGradientDirection;
	}
	
}
